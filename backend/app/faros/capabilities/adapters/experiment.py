# Absolute path to data directory for resolving project paths
_BASE_DIR = os.path.dirname(os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__)))))
_DATA_DIR = os.path.join(_BASE_DIR, "data")


class ExperimentCapability(BaseCapability):
    capability_id = "experiment"
    description = "Provision, execute, and collect results from an experiment code project."
    default_agent_id = "experimenter"
    default_skill_ids = ["experiment-scaffold", "artifact-packaging", "code-execution"]
    artifact_types = ["code_project", "experiment_record", "experiment_report"]

    # ---- scaffold (fallback) ----

    def _build_scaffold_files(
        self, title: str, paper_type: str, framework: str, language: str,
        inputs: Dict[str, Any], selected_candidate: Dict[str, Any],
        candidate_id: str | None, session_id: str | None,
    ) -> List[Dict[str, str]]:
        readme = f"# {title}\n\n"
        readme += "Generated by FAROS experiment capability.\n\n"
        if selected_candidate:
            readme += f"## Problem\n{selected_candidate.get('problem', '')}\n\n"
            readme += f"## Key Insight\n{selected_candidate.get('keyInsight', '')}\n\n"
        readme += "## Structure\n- `src/` runtime code\n- `configs/` experiment configuration\n"
        readme += "- `scripts/` execution entrypoints\n- `tests/` basic smoke tests\n"
        config_json = {
            "title": title,
            "paperType": paper_type,
            "framework": framework,
            "language": language,
            "seedQuery": inputs.get("seedQuery"),
            "selectedCandidateId": candidate_id,
            "ideaSessionId": session_id,
            "objective": selected_candidate.get("problem", ""),
            "hypothesis": selected_candidate.get("keyInsight", ""),
            "methodology": f"Execute {language} code on {framework} framework, collect metrics and figures.",
            "independent_variables": [],
            "dependent_variables": [],
            "controlled_variables": [],
        }
        main_py = (
            '"""Experiment entry point.\n\n'
            f'{selected_candidate.get("problem", "FAROS experiment scaffold")}\n'
            '"""\n'
            'import json, os, sys\n\n'
            'def main():\n'
            '    results = {"status": "completed", "metrics": []}\n'
            '    print(json.dumps(results))\n'
            '    return results\n\n'
            'if __name__ == "__main__":\n'
            '    main()\n'
        )
        return [
            {"path": "README.md", "content": readme},
            {"path": "requirements.txt", "content": "fastapi\nuvicorn\npydantic\nmatplotlib\nnumpy\n"},
            {"path": "configs/experiment.json", "content": json.dumps(config_json, indent=2)},
            {"path": "src/main.py", "content": main_py},
            {"path": "src/pipeline.py", "content": "# Experiment pipeline — extend with custom logic\n"},
            {"path": "scripts/run.sh", "content": "#!/usr/bin/env bash\nset -e\npython src/main.py\n"},
            {"path": "tests/test_smoke.py", "content": "from src.main import main\n\n\ndef test_main_runs():\n    assert main() is not None\n"},
        ]

    # ---- code generation via AgentKernel (when available) ----

    def _generate_code_via_agent(
        self, project_id: str, plan_session_id: str | None, candidate_id: str | None,
        language: str, framework: str, provider_name: str = "qwen", model: str = "qwen-max",
    ) -> bool:
        """Attempt to generate real code via AgentKernel. Returns True if successful."""
        try:
            from app.services.code_agent_service import generate_project_from_plan

            if not plan_session_id or not candidate_id:
                logger.info("No plan session / candidate — skipping AgentKernel code generation")
                return False

            generate_project_from_plan(
                plan_session_id=plan_session_id,
                candidate_id=candidate_id,
                provider_name=provider_name,
                model=model,
                language=language,
                framework=framework,
                enable_web_search=True,
                enable_github=True,
                existing_project_id=project_id,
            )
            logger.info("AgentKernel code generation completed for project %s", project_id)
            return True
        except Exception as exc:
            logger.warning("AgentKernel code generation failed (will use scaffold): %s", exc)
            return False

    # ---- code execution ----

    def _execute_project(
        self, project_id: str, language: str, use_sandbox: bool = True
    ) -> Dict[str, Any]:
        """Execute the project's main entrypoint and collect results.

        Args:
            project_id: Code project identifier.
            language: Programming language.
            use_sandbox: If True, execute via SandboxPool (Docker or subprocess).
                         If False, fall back to direct subprocess.run (legacy path).
        """
        repo_dir = os.path.join(_DATA_DIR, "code_projects", project_id, "repo")
        result = {
            "exit_code": -1,
            "stdout": "",
            "stderr": "",
            "duration_seconds": 0,
            "command": "",
        }

        if language != "python":
            logger.info("No executor available for language=%s — skipping execution", language)
            return result

        main_path = os.path.join(repo_dir, "src", "main.py")
        if not os.path.exists(main_path):
            main_path = os.path.join(repo_dir, "main.py")
        if not os.path.exists(main_path):
            logger.warning("No main.py found for project %s — cannot execute", project_id)
            return result

        command = f"python {os.path.relpath(main_path, repo_dir)}"
        result["command"] = command

        # Try sandbox execution first
        if use_sandbox:
            try:
                return asyncio.run(
                    self._execute_in_sandbox(project_id, repo_dir, command)
                )
            except Exception as exc:
                logger.warning(
                    "Sandbox execution failed for project %s (%s), falling back to direct execution",
                    project_id, exc,
                )

        # Legacy fallback: direct subprocess.run
        return self._execute_direct(main_path, repo_dir, command, result)

    async def _execute_in_sandbox(
        self, project_id: str, repo_dir: str, command: str
    ) -> Dict[str, Any]:
        """Execute via the sandbox pool (Docker or subprocess backend)."""
        from app.code.sandbox import get_sandbox_pool

        result = {
            "exit_code": -1,
            "stdout": "",
            "stderr": "",
            "duration_seconds": 0,
            "command": command,
        }

        pool = await get_sandbox_pool()
        sandbox_id = None
        try:
            sandbox_id = await pool.acquire(repo_dir)
            sandbox_result = await pool.execute(
                sandbox_id, command, timeout=600,
                env={"PYTHONUNBUFFERED": "1"},
            )
            result["exit_code"] = sandbox_result.exit_code
            result["stdout"] = sandbox_result.stdout[-5000:] if len(sandbox_result.stdout) > 5000 else sandbox_result.stdout
            result["stderr"] = sandbox_result.stderr[-5000:] if len(sandbox_result.stderr) > 5000 else sandbox_result.stderr
            result["duration_seconds"] = round(sandbox_result.duration_ms / 1000, 2)
            logger.info(
                "Project %s executed in sandbox: exit=%d duration=%.1fs",
                project_id, result["exit_code"], result["duration_seconds"],
            )
            return result
        finally:
            if sandbox_id:
                await pool.release(sandbox_id)

    def _execute_direct(
        self, main_path: str, repo_dir: str, command: str, result: Dict[str, Any]
    ) -> Dict[str, Any]:
        """Execute directly via subprocess.run (legacy compatibility path)."""
        start = time.time()
        try:
            proc = subprocess.run(
                ["python", main_path],
                capture_output=True,
                text=True,
                timeout=600,
                cwd=repo_dir,
                env={**os.environ, "PYTHONUNBUFFERED": "1"},
            )
            result["exit_code"] = proc.returncode
            result["stdout"] = proc.stdout[-5000:] if len(proc.stdout) > 5000 else proc.stdout
            result["stderr"] = proc.stderr[-5000:] if len(proc.stderr) > 5000 else proc.stderr
            result["duration_seconds"] = round(time.time() - start, 2)
            logger.info(
                "Project executed (direct): exit=%d duration=%.1fs",
                result["exit_code"], result["duration_seconds"],
            )
        except subprocess.TimeoutExpired:
            result["stderr"] = "Execution timed out after 600s"
            logger.warning("Project execution timed out")
        except Exception as exc:
            result["stderr"] = str(exc)
            logger.error("Project execution error: %s", exc)

        return result

    # ---- metrics collection ----

    def _collect_metrics(self, project_id: str, exec_result: Dict[str, Any]) -> List[Dict[str, Any]]:
        """Collect metrics from project execution output and disk files."""
        metrics: List[Dict[str, Any]] = []

        # 1. Try to parse metrics.json from project files
        repo_dir = os.path.join(_DATA_DIR, "code_projects", project_id, "repo")
        metrics_path = os.path.join(repo_dir, "metrics.json")
        if os.path.exists(metrics_path):
            try:
                parsed = json.loads(Path(metrics_path).read_text(encoding="utf-8"))
                items = parsed if isinstance(parsed, list) else [parsed]
                for item in items:
                    if isinstance(item, dict) and "name" in item and "value" in item:
                        metrics.append(item)
            except (json.JSONDecodeError, TypeError):
                pass

        # 2. Try to parse JSON stdout output
        if exec_result.get("stdout"):
            try:
                parsed = json.loads(exec_result["stdout"].strip())
                if isinstance(parsed, dict) and parsed.get("metrics"):
                    for m in parsed["metrics"]:
                        if isinstance(m, dict) and "name" in m and "value" in m:
                            if not any(x["name"] == m["name"] for x in metrics):
                                metrics.append(m)
            except (json.JSONDecodeError, TypeError):
                pass

        # 3. Always add execution metadata
        metrics.append({"name": "exit_code", "value": exec_result.get("exit_code", -1)})
        if exec_result.get("duration_seconds"):
            metrics.append({"name": "duration_seconds", "value": exec_result["duration_seconds"], "unit": "s"})

        return metrics

    # ---- report generation ----

    def _generate_experiment_report(
        self, project_id: str, project_title: str, experiment_id: str,
        exec_result: Dict[str, Any], metrics: List[Dict[str, Any]],
        experiment_design: Dict[str, Any] | None,
    ) -> str:
        """Generate a Markdown experiment report and save it to the project directory."""
        from app.services.experiment_report_service import get_experiment_report_service

        svc = get_experiment_report_service()
        report_md = svc.generate_minimal_report(
            project_id=project_id,
            project_title=project_title,
            metrics=metrics,
        )

        # Save to project directory
        repo_dir = os.path.join(_DATA_DIR, "code_projects", project_id, "repo")
        os.makedirs(repo_dir, exist_ok=True)
        report_path = os.path.join(repo_dir, "experiment_report.md")
        with open(report_path, "w", encoding="utf-8") as f:
            f.write(report_md)
        logger.info("Experiment report saved to %s", report_path)

        return report_md

    # ---- public API ----

    def build_provider_task(
        self, context: ExecutionContext, inputs: Dict[str, Any], binding: CapabilityBinding,
    ) -> ProviderTask | None:
        if binding.provider_type != "execution":
            return None
        selected_candidate = inputs.get("selectedCandidate") or {}
        session_id = inputs.get("ideaSessionId")
        candidate_id = inputs.get("selectedCandidateId")
        paper_type = inputs.get("paperType", "algorithm")
        title = selected_candidate.get("title") or inputs.get("seedQuery") or "FAROS Experiment"
        description = selected_candidate.get("problem") or inputs.get("notes") or ""
        language = inputs.get("language", "python")
        framework = inputs.get("framework", "FastAPI")
        return ProviderTask(
            capability_id=self.capability_id,
            provider=binding.provider,
            model=binding.model,
            options={
                "title": title,
                "description": description,
                "language": language,
                "framework": framework,
                "paperType": paper_type,
                "files": self._build_scaffold_files(
                    title, paper_type, framework, language, inputs,
                    selected_candidate, candidate_id, session_id,
                ),
            },
        )

    def execute(self, context: ExecutionContext, inputs: Dict[str, Any]) -> CapabilityResult:
        selected_candidate = inputs.get("selectedCandidate") or {}
        session_id = inputs.get("ideaSessionId")
        candidate_id = inputs.get("selectedCandidateId")
        paper_type = inputs.get("paperType", "algorithm")
        title = selected_candidate.get("title") or inputs.get("seedQuery") or "FAROS Experiment"
        description = selected_candidate.get("problem") or inputs.get("notes") or ""
        language = inputs.get("language", "python")
        framework = inputs.get("framework", "FastAPI")
        return self._materialize_workspace(
            context=context, title=title, description=description,
            language=language, framework=framework, paper_type=paper_type,
            session_id=session_id, candidate_id=candidate_id,
            files=self._build_scaffold_files(
                title, paper_type, framework, language, inputs,
                selected_candidate, candidate_id, session_id,
            ),
            event_message=None,
        )

    def consume_provider_result(
        self, context: ExecutionContext, inputs: Dict[str, Any], provider_result: ProviderResult,
    ) -> CapabilityResult:
        payload = provider_result.payload
        selected_candidate = inputs.get("selectedCandidate") or {}
        session_id = inputs.get("ideaSessionId")
        candidate_id = inputs.get("selectedCandidateId")
        paper_type = inputs.get("paperType", "algorithm")
        title = selected_candidate.get("title") or inputs.get("seedQuery") or "FAROS Experiment"
        description = selected_candidate.get("problem") or inputs.get("notes") or ""
        return self._materialize_workspace(
            context=context, title=title, description=description,
            language=inputs.get("language", "python"),
            framework=inputs.get("framework", "FastAPI"),
            paper_type=paper_type, session_id=session_id,
            candidate_id=candidate_id,
            files=payload.get("files", []),
            event_message=provider_result.text,
            project_title_override=payload.get("projectTitle"),
        )

    def _materialize_workspace(
        self, context: ExecutionContext, title: str, description: str,
        language: str, framework: str, paper_type: str,
        session_id: str | None, candidate_id: str | None,
        files: List[Dict[str, str]], event_message: str | None,
        project_title_override: str | None = None,
    ) -> CapabilityResult:
        init_db()

        with get_session_context() as db:
            project = code_project_service.create_project(
                db=db,
                title=project_title_override or f"{title} [{language}]",
                description=description,
                language=language,
                framework=framework,
                source_idea_session_id=session_id,
                source_candidate_id=candidate_id,
            )
            project_id = project.id
            project_title = project.title

            # Write scaffold files first
            code_project_service.write_project_files(db, project.id, files)

        # Attempt AgentKernel code generation
        agent_generated = self._generate_code_via_agent(
            project_id=project_id,
            plan_session_id=session_id,
            candidate_id=candidate_id,
            language=language,
            framework=framework,
        )

        # Execute the generated code
        exec_result = self._execute_project(project_id, language)

        # Collect metrics
        metrics = self._collect_metrics(project_id, exec_result)

        # Collect figures from project directory
        figures: List[Dict[str, Any]] = []
        figures_dir = os.path.join(_DATA_DIR, "code_projects", project_id, "repo", "figures")
        if os.path.isdir(figures_dir):
            for f in sorted(os.listdir(figures_dir)):
                if f.lower().endswith((".png", ".jpg", ".jpeg", ".pdf", ".svg")):
                    figures.append({
                        "title": os.path.splitext(f)[0].replace("_", " ").title(),
                        "path": f"figures/{f}",
                        "description": "",
                    })

        # Experiment design from config
        experiment_design = None
        config_path = os.path.join(_DATA_DIR, "code_projects", project_id, "repo", "configs", "experiment.json")
        if os.path.exists(config_path):
            try:
                experiment_design = json.loads(Path(config_path).read_text(encoding="utf-8"))
            except (json.JSONDecodeError, TypeError):
                pass

        # Create experiment record
        experiment = create_experiment({
            "name": f"{title} Experiment",
            "projectId": project_id,
            "description": description,
            "tags": ["faros", "llm", paper_type, f"lang:{language}"],
            "status": "completed" if exec_result.get("exit_code") == 0 else "completed_with_errors",
        })

        # Generate MD report
        report_md = self._generate_experiment_report(
            project_id=project_id,
            project_title=project_title,
            experiment_id=experiment["id"],
            exec_result=exec_result,
            metrics=metrics,
            experiment_design=experiment_design,
        )

        # Build structured metric snapshots for downstream consumption
        metrics_json_path = os.path.join(_DATA_DIR, "code_projects", project_id, "repo", "metrics.json")
        if not os.path.exists(metrics_json_path) and metrics:
            with open(metrics_json_path, "w", encoding="utf-8") as f:
                json.dump(metrics, f, indent=2, ensure_ascii=False)

        events = [
            {"level": "info", "message": event_message or f"Experiment stage completed for project {project_id}"},
        ]
        if agent_generated:
            events.append({"level": "info", "message": "AgentKernel code generation succeeded"})
        events.append({
            "level": "info",
            "message": f"Execution: exit_code={exec_result.get('exit_code')}, "
                       f"duration={exec_result.get('duration_seconds', 0):.1f}s, "
                       f"metrics_count={len(metrics)}, figures_count={len(figures)}",
        })

        return CapabilityResult(
            status="completed",
            outputs={
                "projectId": project_id,
                "projectTitle": project_title,
                "experimentId": experiment["id"],
                "experimentStatus": experiment.get("status"),
                "experimentIds": [experiment["id"]],
                # New: structured data for downstream Paper/Review modules
                "metrics": metrics,
                "metricsJsonPath": f"code_projects/{project_id}/repo/metrics.json",
                "figures": figures,
                "figurePaths": [f["path"] for f in figures],
                "experimentDesign": experiment_design,
                "reportMdPath": f"code_projects/{project_id}/repo/experiment_report.md",
                "executionSummary": {
                    "exitCode": exec_result.get("exit_code"),
                    "durationSeconds": exec_result.get("duration_seconds"),
                    "command": exec_result.get("command"),
                },
                "agentGenerated": agent_generated,
            },
            artifacts=[
                ArtifactRecord(
                    id=f"{context.run_id}:{self.capability_id}:project",
                    type="code_project",
                    uri=f"project://{project_id}",
                    producer=self.capability_id,
                    summary=f"Project {project_id} created for experiment stage",
                    metadata={"projectId": project_id, "language": language, "framework": framework},
                ),
                ArtifactRecord(
                    id=f"{context.run_id}:{self.capability_id}:experiment",
                    type="experiment_record",
                    uri=f"experiment://{experiment['id']}",
                    producer=self.capability_id,
                    summary=f"Experiment {experiment['id']} linked to project {project_id}",
                    metadata={"experimentId": experiment["id"], "projectId": project_id},
                ),
                ArtifactRecord(
                    id=f"{context.run_id}:{self.capability_id}:report",
                    type="experiment_report",
                    uri=f"file://code_projects/{project_id}/repo/experiment_report.md",
                    producer=self.capability_id,
                    summary=f"MD experiment report for project {project_id}",
                    metadata={"projectId": project_id, "path": f"code_projects/{project_id}/repo/experiment_report.md"},
                ),
            ],
            events=events,
        )
